CREATE TABLE TEMP_BATCH_MSG
(
    PROC_CD                        NVARCHAR2(40) NOT NULL,
    PROC_DATE                      DATE NOT NULL,
    PROC_SEQ                       NUMBER DEFAULT 0 NOT NULL,
    SEQ                            NUMBER DEFAULT 0 NOT NULL,
    FLG                            NUMBER DEFAULT 0 NOT NULL,
    MSG                            NVARCHAR2(255),
    CHECK_FLG                      NUMBER DEFAULT 0 NOT NULL,
    INPUT_DATE                     DATE NOT NULL,
    INPUTOR_CD                     NVARCHAR2(10),
    UPDATE_DATE                    DATE NOT NULL,
    UPDATOR_CD                     NVARCHAR2(10),
    CONSTRAINT PK_TEMP_BATCH_MSG PRIMARY KEY (PROC_CD, PROC_DATE, PROC_SEQ, SEQ) USING INDEX
)
/
COMMENT ON TABLE TEMP_BATCH_MSG IS 'バッチプログラムメッセージ'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.PROC_CD IS 'プロシージャ名'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.PROC_DATE IS '処理日'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.PROC_SEQ IS '処理連番'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.SEQ IS '連番'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.FLG IS 'フラグ'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.MSG IS 'メッセージ'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.CHECK_FLG IS '確認フラグ|0:未確認 1:確認済み'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.INPUT_DATE IS '登録日時'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.INPUTOR_CD IS '登録者ID'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.UPDATE_DATE IS '更新日時'
/
COMMENT ON COLUMN TEMP_BATCH_MSG.UPDATOR_CD IS '更新者ID'
/
