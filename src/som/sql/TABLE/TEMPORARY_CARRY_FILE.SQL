CREATE TABLE TEMPORARY_CARRY_FILE
(
    INPUTOR_CD                     NVARCHAR2(10),
    INPUT_DATE                     DATE,
    DATA_CLS                       CHAR(2),
    ROW_NUM                        NUMBER,
    CSV_DATA                       NVARCHAR2(4000)
)
PCTFREE 10
MAXTRANS 255
TABLESPACE SOM
STORAGE(INITIAL 64K NEXT 1M MINEXTENTS 1 MAXEXTENTS 2147483645 BUFFER_POOL DEFAULT)
NOCACHE
LOGGING
/
COMMENT ON TABLE TEMPORARY_CARRY_FILE IS '運送会社連携ファイル出力用一時表'
/
COMMENT ON COLUMN TEMPORARY_CARRY_FILE.INPUTOR_CD IS '入力者'
/
COMMENT ON COLUMN TEMPORARY_CARRY_FILE.INPUT_DATE IS '入力日'
/
COMMENT ON COLUMN TEMPORARY_CARRY_FILE.DATA_CLS IS 'データ区分|00:ヘッダ,01:内容'
/
COMMENT ON COLUMN TEMPORARY_CARRY_FILE.ROW_NUM IS '行番号'
/
COMMENT ON COLUMN TEMPORARY_CARRY_FILE.CSV_DATA IS 'CSV内容'
/
