CREATE OR REPLACE FUNCTION      FUN_GET_CLAIM_NO_KARI
	(
		 P_STR_UPDATOR_CD		IN	NVARCHAR2
	)
RETURN	NVARCHAR2
IS
/*----------------------------------------------------------------------------------------------------------------
