	date		ver		name				comments
	----------	------	------------------	----------------------------------------------------------------------
	2008.08.05	1.00	TOSCO       		creation
*/----------------------------------------------------------------------------------------------------------------

	--------------------------------------------------------------------------------
	--定数・変数宣言
	--------------------------------------------------------------------------------
	c_Key					CONSTANT SLIP_NUMBER.KEY%TYPE				:= 202;			-- 仮締め用請求番号KEY

	nvClaimNoKari			TEMPORARY_CLAIM_HEADER.CLAIM_NO%TYPE;						-- 仮締め用請求番号

BEGIN
	--------------------------------------------------------------------------------
	--★仮締め用請求番号取得
	--------------------------------------------------------------------------------
	nvClaimNoKari := FUN_GET_SLIP_NO(c_Key, P_STR_UPDATOR_CD);

