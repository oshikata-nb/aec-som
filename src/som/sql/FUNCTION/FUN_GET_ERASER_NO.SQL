(history)
	date		ver		name				comments
	----------	------	------------------	----------------------------------------------------------------------
	2008.07.28	1.00	TOSCO       		creation
*/----------------------------------------------------------------------------------------------------------------

	--------------------------------------------------------------------------------
	--定数・変数宣言
	--------------------------------------------------------------------------------
	c_Key					CONSTANT SLIP_NUMBER.KEY%TYPE				:= 106;			-- 消込番号KEY

	nvEraserNo				ERASER_HEADER.ERASER_NO%TYPE;								-- 消込番号

BEGIN
	--------------------------------------------------------------------------------
	--★消込番号取得
	--------------------------------------------------------------------------------
	nvEraserNo := FUN_GET_SLIP_NO(c_Key, P_STR_UPDATOR_CD);

