CREATE OR REPLACE FUNCTION      FUN_GET_DEPOSIT_NO
	(
		 P_STR_UPDATOR_CD		IN	NVARCHAR2
	)
RETURN	NVARCHAR2
IS
/*----------------------------------------------------------------------------------------------------------------
	種別		:	FUNCTION
	名称		:	FUN_GET_DEPOSIT_NO
	処理内容	:	売掛番号取得処理
	引数		:	P_STR_UPDATOR_CD		登録者ＩＤ	NVARCHAR2型
	戻値		:	nvDepositNo				売掛番号	NVARCHAR2型
	VERNO.		:	1.00
(history)
	date		ver		name				comments
	----------	------	------------------	----------------------------------------------------------------------
	2008.07.28	1.00	TOSCO       		creation
*/----------------------------------------------------------------------------------------------------------------

	--------------------------------------------------------------------------------
	--定数・変数宣言
	--------------------------------------------------------------------------------
	c_Key					CONSTANT SLIP_NUMBER.KEY%TYPE				:= 101;			-- 売掛番号KEY

	nvDepositNo				DEPOSIT_HEADER.DEPOSIT_NO%TYPE;								-- 売掛番号

BEGIN
	--------------------------------------------------------------------------------
	--★売掛番号取得
	--------------------------------------------------------------------------------
	nvDepositNo := FUN_GET_SLIP_NO(c_Key, P_STR_UPDATOR_CD);

	RETURN nvDepositNo;
EXCEPTION
	--------------------------------------------------------------------------------
	--例外処理
	--------------------------------------------------------------------------------
	WHEN OTHERS THEN
		RAISE;

END FUN_GET_DEPOSIT_NO;
