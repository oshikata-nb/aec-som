CREATE OR REPLACE FUNCTION      FUN_GET_PAYMENT_NO
	(
		 P_STR_UPDATOR_CD		IN	NVARCHAR2
	)
RETURN	NVARCHAR2
IS
/*----------------------------------------------------------------------------------------------------------------
	種別		:	FUNCTION
	名称		:	FUN_GET_PAYMENT_NO
	処理内容	:	支払番号取得処理
	引数		:	P_STR_UPDATOR_CD		登録者ＩＤ	NVARCHAR2型
	戻値		:	nvPaymentNo				支払番号	NVARCHAR2型
	VERNO.		:	1.00
(history)
	date		ver		name				comments
	----------	------	------------------	----------------------------------------------------------------------
	2008.07.28	1.00	TOSCO       		creation
*/----------------------------------------------------------------------------------------------------------------

	--------------------------------------------------------------------------------
	--定数・変数宣言
	--------------------------------------------------------------------------------
	c_Key					CONSTANT SLIP_NUMBER.KEY%TYPE				:= 104;			-- 支払番号KEY

	nvPaymentNo				PAYMENT_HEADER.PAYMENT_NO%TYPE;								-- 支払番号

BEGIN
	--------------------------------------------------------------------------------
	--★支払番号取得
	--------------------------------------------------------------------------------
	nvPaymentNo := FUN_GET_SLIP_NO(c_Key, P_STR_UPDATOR_CD);

	RETURN nvPaymentNo;
EXCEPTION
	--------------------------------------------------------------------------------
	--例外処理
	--------------------------------------------------------------------------------
	WHEN OTHERS THEN
		RAISE;

END FUN_GET_PAYMENT_NO;
/
