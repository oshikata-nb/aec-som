CREATE OR REPLACE FUNCTION      FUN_GET_PAYABLE_NO
	(
		 P_STR_UPDATOR_CD		IN	NVARCHAR2
	)
RETURN	NVARCHAR2
IS
/*----------------------------------------------------------------------------------------------------------------
	種別		:	FUNCTION
	名称		:	FUN_GET_PAYABLE_NO
	処理内容	:	買掛番号取得処理
	引数		:	P_STR_UPDATOR_CD		登録者ＩＤ	NVARCHAR2型
	戻値		:	nvPayableNo				買掛番号	NVARCHAR2型
	VERNO.		:	1.00
(history)
	date		ver		name				comments
	----------	------	------------------	----------------------------------------------------------------------
	2008.07.28	1.00	TOSCO       		creation
*/----------------------------------------------------------------------------------------------------------------

	--------------------------------------------------------------------------------
	--定数・変数宣言
	--------------------------------------------------------------------------------
	c_Key					CONSTANT SLIP_NUMBER.KEY%TYPE				:= 103;			-- 買掛番号KEY

	nvPayableNo				PAYABLE_HEADER.PAYABLE_NO%TYPE;								-- 買掛番号

BEGIN
	--------------------------------------------------------------------------------
	--★買掛番号取得
	--------------------------------------------------------------------------------
	nvPayableNo := FUN_GET_SLIP_NO(c_Key, P_STR_UPDATOR_CD);

	RETURN nvPayableNo;
EXCEPTION
	--------------------------------------------------------------------------------
	--例外処理
	--------------------------------------------------------------------------------
	WHEN OTHERS THEN
		RAISE;

END FUN_GET_PAYABLE_NO;
