CREATE OR REPLACE FUNCTION AP21.FUN_GET_CLAIM_NO
	(
		 P_STR_UPDATOR_CD		IN	NVARCHAR2
	)
RETURN	NVARCHAR2
IS
/*----------------------------------------------------------------------------------------------------------------
	種別		:	FUNCTION
	名称		:	FUN_GET_CLAIM_NO
	処理内容	:	請求番号取得処理
	引数		:	P_STR_UPDATOR_CD		登録者ＩＤ	NVARCHAR2型
	戻値		:	nvClaimNo				請求番号	NVARCHAR2型
	VERNO.		:	1.00
(history)
	date		ver		name				comments
	----------	------	------------------	----------------------------------------------------------------------
	2008.08.05	1.00	TOSCO       		creation
*/----------------------------------------------------------------------------------------------------------------

	--------------------------------------------------------------------------------
	--定数・変数宣言
	--------------------------------------------------------------------------------
	c_Key					CONSTANT SLIP_NUMBER.KEY%TYPE				:= 102;			-- 請求番号KEY

	nvClaimNo				CLAIM_HEADER.CLAIM_NO%TYPE;									-- 請求番号

BEGIN
	--------------------------------------------------------------------------------
	--★請求番号取得
	--------------------------------------------------------------------------------
	nvClaimNo := FUN_GET_SLIP_NO(c_Key, P_STR_UPDATOR_CD);

	RETURN nvClaimNo;
EXCEPTION
	--------------------------------------------------------------------------------
	--例外処理
	--------------------------------------------------------------------------------
	WHEN OTHERS THEN
		RAISE;

END FUN_GET_CLAIM_NO;
