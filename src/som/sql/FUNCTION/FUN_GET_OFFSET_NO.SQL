CREATE OR REPLACE FUNCTION      FUN_GET_OFFSET_NO
	(
		 P_STR_UPDATOR_CD		IN	NVARCHAR2
	)
RETURN	NVARCHAR2
IS
/*----------------------------------------------------------------------------------------------------------------
	種別		:	FUNCTION
	名称		:	FUN_GET_OFFSET_NO
	処理内容	:	相殺番号取得処理
	引数		:	P_STR_UPDATOR_CD		登録者ＩＤ	NVARCHAR2型
	戻値		:	nvOffsetNo				相殺番号	NVARCHAR2型
	VERNO.		:	1.00
(history)
	date		ver		name				comments
	----------	------	------------------	----------------------------------------------------------------------
	2008.07.28	1.00	TOSCO       		creation
*/----------------------------------------------------------------------------------------------------------------

	--------------------------------------------------------------------------------
	--定数・変数宣言
	--------------------------------------------------------------------------------
	c_Key					CONSTANT SLIP_NUMBER.KEY%TYPE				:= 105;			-- 相殺番号KEY

	nvOffsetNo				OFFSET_GROUP_HEADER.OFFSET_NO%TYPE;							-- 相殺番号

BEGIN
	--------------------------------------------------------------------------------
	--★相殺番号取得
	--------------------------------------------------------------------------------
	nvOffsetNo := FUN_GET_SLIP_NO(c_Key, P_STR_UPDATOR_CD);

	RETURN nvOffsetNo;
EXCEPTION
	--------------------------------------------------------------------------------
	--例外処理
	--------------------------------------------------------------------------------
	WHEN OTHERS THEN
		RAISE;

END FUN_GET_OFFSET_NO;
/
