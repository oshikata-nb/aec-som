/**
 * 製造指図－フォーミュラ-詳細検索用SQL
 *
 * @author tosco
 *
 * entityName=DirectionDirectionFormulaList
 * packageName=direction
 * methodName=getByPrimaryKey
 *
 */
SELECT
	FORMULA.DIRECTION_DIVISION
,	FORMULA.DIRECTION_NO
,	FORMULA.STEP_NO
,	FORMULA.LINE_NO
,	FORMULA.SEQ
,	FORMULA.LINE_TYPE
,	FORMULA.ITEM_CD
,	FORMULA.TONYU
,	FORMULA.DATAREAD
,	FORMULA.TONYUSOKUDO
,	FORMULA.QTY
,	FORMULA.COST
,	FORMULA.STEP_CONDITION
,	FORMULA.NOTES
,	FORMULA.LOT_NO
,	FORMULA.REMARK
,	FORMULA.INPUTOR_CD
,	FORMULA.INPUT_DATE
,	FORMULA.UPDATOR_CD
,	FORMULA.UPDATE_DATE
,   ITEM.ITEM_NAME                    AS ITEM_NAME
,   ITEM.UNIT_OF_OPERATION_MANAGEMENT AS UNIT_DIVISION
,   NAME.NAME01                       AS UNIT_NAME
FROM
    DIRECTION_FORMULA FORMULA,
    ITEM ITEM,
    NAMES NAME
WHERE
	    FORMULA.DIRECTION_DIVISION = 1
	AND FORMULA.DIRECTION_NO = /*directionNo*/'HAMA000001'
	AND FORMULA.STEP_NO = /*stepNo*/1
	AND FORMULA.LINE_NO = /*lineNo*/1
    AND ITEM.ITEM_CD(+) = FORMULA.ITEM_CD
    AND NAME.NAME_DIVISION(+) = 'UNIT'
    AND NAME.NAME_CD(+) = ITEM.UNIT_OF_OPERATION_MANAGEMENT

