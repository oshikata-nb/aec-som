     HEAD.DIRECTION_DIVISION
    ,HEAD.DIRECTION_NO
    ,HEAD.DIRECTION_DATE
    ,HEAD.ASP_ORDER_NO
    ,HEAD.DIRECTION_STATUS
    ,HEAD.RECIPE_ID
    ,HEAD.RECIPE_CD
    ,HEAD.RECIPE_VERSION
    ,HEAD.PRODUCTION_LINE
    ,HEAD.COMPOUND_TANK_NO
    ,HEAD.HOLD_TANK_NO
    ,HEAD.DISSOLUTION_TANK_NO
    ,HEAD.FILLTANK_NO
    ,HEAD.PACKAGE_LINE
    ,HEAD.CURRENT_STEP_NO
    ,HEAD.ITEM_CD
    ,HEAD.PLANED_QTY
    ,HEAD.RESULT_QTY
    ,HEAD.DIVIDE_FLAG
    ,HEAD.NEXT_PLANED_QTY
    ,HEAD.PDW_RESULT
    ,HEAD.PEOCESS_LOSS
    ,HEAD.LOT_NO
    ,HEAD.PLANED_SDATE
    ,HEAD.PLANED_EDATE
    ,HEAD.RESULT_SDATE
    ,HEAD.RESULT_EDATE
    ,HEAD.STERIT_SDATE
    ,HEAD.STERIT_EDATE
    ,HEAD.MEKKIN_TANK_TEMP_MIN
    ,HEAD.MEKKIN_TANK_TEMP_MAX
    ,HEAD.HAISUI_CHECK
    ,HEAD.STAMP_FLAG
    ,HEAD.STAMP_DATE
    ,HEAD.STAMP_TANTO_CD
    ,HEAD.PRODUCT_LABEL_FLAG
    ,HEAD.PRODUCT_LABEL_DATE
    ,HEAD.PRODUCT_LABEL_TANTO_CD
    ,HEAD.PRODUCT_RECORD_FLAG
    ,HEAD.PRODUCT_RECORD_DATE
    ,HEAD.PRODUCT_RECORD_TANTO_CD
    ,HEAD.STOCKDISS_LABEL_FLAG
    ,HEAD.STOCKDISS_LABEL_DATE
    ,HEAD.STOCKDISS_LABEL_TANTO_CD
    ,HEAD.CERTIFICATION_FLAG
    ,HEAD.CERTIFICATION_DATE
    ,HEAD.REMARK
    ,HEAD.NOTES
    ,HEAD.DELETE_FLAG
    ,HEAD.SEIZOTANTOCODE
    ,HEAD.SENJOTANTOCODE
    ,HEAD.MEKKINTANTOCODE
