    ,HEAD.YOBIYOKAICONDI
    ,HEAD.HOLDTANKCONDI
    ,HEAD.TOTAL_JOBTIME
    ,HEAD.ENPLOY_JOBTIME
    ,HEAD.COOPER_JOBTIME
    ,HEAD.APP_TANTO_CD
    ,HEAD.APP_DATE
    ,HEAD.LAST_APP_TANTO_CD
    ,HEAD.LAST_APP_DATE
    ,HEAD.INPUT_DATE
    ,HEAD.INPUTOR_CD
    ,HEAD.UPDATE_DATE
    ,HEAD.UPDATOR_CD
    ,ITEM.ITEM_NAME AS             ITEM_NAME
    ,ITEM.OTHER_COMPANY_CD1 AS     OTHER_COMPANY_CD1
    ,ITEM.SHIPPER_DIVISION AS      SHIPPER_DIVISION
    ,ITEM.UNIT_OF_OPERATION_MANAGEMENT AS UNIT_OF_OPERATION_MANAGEMENT
    ,LINE.PRODUCTION_LINE_NAME AS  PRODUCTION_LINE_NAME
    ,NAME.NAME01 AS                UNIT_NAME
    ,TAN1.TANTO_NM AS              SEIZOTANTO_NAME
    ,TAN2.TANTO_NM AS              SENJOTANTO_NAME
    ,TAN3.TANTO_NM AS              MEKKINTANTO_NAME
    ,RCPHEAD.RECIPE_NAME AS        RECIPE_NAME

FROM
    LOGIN TAN1,
    LOGIN TAN2,
    LOGIN TAN3,
    VALID_ITEM_QUEUE MAX_ITEM,
    LINE LINE,
    NAMES NAME,
    VALID_ITEM_QUEUE ITEM,
    RECIPE_HEADER RCPHEAD,
	DIRECTION_HEADER HEAD
WHERE
        HEAD.DIRECTION_DIVISION = '1'
	AND HEAD.DIRECTION_NO = /*directionNo*/
    AND MAX_ITEM.ITEM_CD(+) = HEAD.ITEM_CD
    AND ITEM.ITEM_CD(+) = MAX_ITEM.ITEM_CD
    AND ITEM.VERSION(+) = MAX_ITEM.VERSION
    AND LINE.PRODUCTION_LINE(+) = HEAD.PRODUCTION_LINE
    AND NAME.NAME_DIVISION(+) = 'UNIT'
    AND NAME.NAME_CD(+) = ITEM.UNIT_OF_OPERATION_MANAGEMENT
    AND TAN1.TANTO_CD(+) = HEAD.SEIZOTANTOCODE
    AND TAN2.TANTO_CD(+) = HEAD.SENJOTANTOCODE
    AND TAN3.TANTO_CD(+) = HEAD.MEKKINTANTOCODE
    AND RCPHEAD.RECIPE_ID(+) = HEAD.RECIPE_ID
    