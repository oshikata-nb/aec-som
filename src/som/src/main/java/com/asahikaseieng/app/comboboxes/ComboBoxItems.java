package com.asahikaseieng.app.comboboxes;

/**
 * ##ここにクラスの説明を書いてください##
 * @author a7710658
 */
public class ComboBoxItems {
	private String values;

	private String labales;

	/**
	 * コンストラクタ
	 */
	public ComboBoxItems() {
	}

	/**
	 * labalesを取得します。
	 * @return labales
	 */
	public String getLabales() {
		return labales;
	}

	/**
	 * labalesを設定します。
	 * @param labales labales
	 */
	public void setLabales(final String labales) {
		this.labales = labales;
	}

	/**
	 * valuesを取得します。
	 * @return values
	 */
	public String getValues() {
		return values;
	}

	/**
	 * valuesを設定します。
	 * @param values values
	 */
	public void setValues(final String values) {
		this.values = values;
	}

}
